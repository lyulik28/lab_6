/**
 * Створення інтерфейсу SetAndGet,
 * який має метод setSurname(String surname), getSurname()
 * setName(String name), getName(), setAge(int age), getAge()
 */
public interface SetAndGet {
    void setSurname(String surname);
    String getSurname();
    void setName(String name);
    String getName();
    void setAge(int age);
    int getAge();
}
